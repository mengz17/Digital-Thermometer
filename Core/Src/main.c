/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	set_temp = 2400; //Initialization of set temp
	mode = 1; //Display actual temperature mode
	Upressed = 0; // Initialization of GPIO variable
	Dpressed = 0;
	Cpressed = 0;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	BSP_LCD_GLASS_Init();
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM4_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
	HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1); // Start PWM
	HAL_TIM_Base_Start_IT(&htim3);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		if(mode == 1 && Cpressed == 1){ //When in actual temp display mode and selection is pressed
			mode = 2;
			Cpressed = 0;
		}
		
		if(mode == 2 && Cpressed == 1){ //When in setting mode and selection is pressed
			mode = 1;
			Cpressed = 0;
		}
		
		if(mode == 1){
			HAL_ADC_Start(&hadc1); //Start ADC
			HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY); //ADC Conversion
			raw_temp = HAL_ADC_GetValue(&hadc1); //Give value to raw temp
			real_temp = raw_temp * 2.44141; //Convert raw temperature to real temerature
			sprintf(lcd_buffer1, "%d C", real_temp);
			BSP_LCD_GLASS_DisplayString((uint8_t*)lcd_buffer1 );
			BSP_LCD_GLASS_DisplayChar((uint8_t*)&lcd_buffer1[1], POINT_ON, DOUBLEPOINT_OFF, LCD_DIGIT_POSITION_2);
			HAL_Delay(300);
		}
		
		if(mode == 2){
			sprintf(lcd_buffer2,"%d", set_temp);
			BSP_LCD_GLASS_DisplayString((uint8_t*)lcd_buffer2);
			BSP_LCD_GLASS_DisplayChar((uint8_t*)&lcd_buffer2[1], POINT_ON, DOUBLEPOINT_OFF, LCD_DIGIT_POSITION_2);
		}
		if(Upressed == 1){ //When joy stick up is pressed
			set_temp += 50; //Temperature + 0.5 degree
			Upressed = 0;
		}
		if(Dpressed == 1){
			set_temp -= 50; // Temerature - 0.5 degree
			Dpressed = 0;
		}
		temp_control = real_temp - set_temp; //Difference of actual temerature and set temerature
		temp_control = temp_control / 10; //Convert the difference to an acceptable range
		temp_control += 14; //Since fan starts at value 15
		htim4.Instance -> CCR1 = temp_control;
	}
  /* USER CODE END 3 */
}

